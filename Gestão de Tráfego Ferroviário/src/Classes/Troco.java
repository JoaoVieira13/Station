package Classes;

public class Troco {
    private String id_primeira_estacao;
    private String id_ultima_estacao;
    private String nome;

    public String getId_primeira_estacao() {
        return this.id_primeira_estacao;
    }

    public void setId_primeira_estacao(String id_primeira_estacao) {
        this.id_primeira_estacao = id_primeira_estacao;
    }

    public String getId_ultima_estacao() {
        return this.id_ultima_estacao;
    }

    public void setId_ultima_estacao(String id_ultima_estacao) {
        this.id_ultima_estacao = id_ultima_estacao;
    }

    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
